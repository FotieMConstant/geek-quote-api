<template>
  <div class="home text-left">
    <ApiReference
      :configuration="{
        spec: { url: '/resource/openapi-spec.json' },
        proxy: 'https://api.scalar.com/request-proxy',
        footerBelowSidebar: false,
        theme: selectedTheme,
      }"
    />
  </div>
</template>

<script lang="ts">
import { ApiReference } from "@scalar/api-reference";

import { defineComponent } from "vue";
import lanaConfig from "../../lana.config.json";

export default defineComponent({
  name: "HomeView",
  components: {
    ApiReference,
  },
  data() {
    return {
      lanaConfig, // Add this line to include lanaConfig in the data
      selectedTheme: "", // dev's selected theme
    };
  },
  beforeMount() {
    this.selectedTheme = lanaConfig.theme || "default";
  },
  mounted() {
    console.log("Dev theme from condig => ", this.selectedTheme);
    this.updateElementContentByClass(
      "darklight-reference-promo",
      "Generated by lanadoc"
    );
  },
  methods: {
    updateElementContentByClass(className: string, content: string) {
      const elements = document.getElementsByClassName(className);
      for (let i = 0; i < elements.length; i++) {
        const element = elements[i] as HTMLAnchorElement;
        element.textContent = content;
        element.href = "https://github.com/FotieMConstant/lanadoc-cli";
        // console.log(element);
      }
    },
  },
});
</script>
